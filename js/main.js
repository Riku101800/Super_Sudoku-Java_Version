$(document).ready(function() {
    //randomly select current puzzle
    if($("#cheatMode").val() == "true") {
        var current_puzzle = cheatPuzzle_start;
        var current_solution = cheatPuzzle_solution;
    }
    else {
        var index = Math.floor(Math.random() * 9);
        var current_puzzle = allPuzzlesStart[index];
        var current_solution = allPuzzlesSolution[index];
    }

    var table = $(".normal");
    for(var x = 0; x < 9; x++) {
        for(var y = 0; y < 9; y++) {
            if(current_puzzle[x][y] != 0) {
                table.find("#"+ x+"-"+ y).html(current_puzzle[x][y]);
                document.getElementById(x+"-"+y).className = "start";
            }
        }
    }

    $(window).keydown(function(event){
        if(event.keyCode == 13) {
            event.preventDefault();
            return false;
        }
    });

    //on cell click
    table.find("td").click(function () {
        if(event.target.className != "start") {
            var id = event.target.id;
            if(id == null || id == ""){
                var object = event.target;
                var parent = $(object).parent();
            }
            document.getElementById('popupBox').style.display = "block";
            $("#answer").click(function () {
                var answer = $("#cellAnswer").val();
                if(answer < 10 && answer > 0) {
                    table.find("#" + id).find(".noteTable").remove();
                    table.find("#" + id).html(answer);
                    document.getElementById('popupBox').style.display = "none";
                    if (id != null) {
                        var arr = id.split('-');
                        var x = arr[0];
                        var y = arr[1];
                        if (current_solution[x][y] == answer) {
                            document.getElementById(id).className = "correct";
                        }
                        else {
                            document.getElementById(id).className = "incorrect";
                        }
                        current_puzzle[x][y] = parseInt(answer);
                    }

                    id = null;
                }
            });

            $("#clues").click(function () {
                var checkedValues = $("#notes").find('input:checkbox:checked').map(function() {
                    return this.value;
                }).get();

                var count = 1;

                var html = "<table class='noteTable'>";
                for(var x = 0; x <= 2; x++) {
                    html += "<tr>";
                    for(var y = 0; y <= 2; y++) {
                        if(($.inArray(count.toString(), checkedValues)) != -1) {
                            html += "<td>" + count.toString() + "</td>";
                        }
                        else {
                            html += "<td></td>";
                        }
                        count += 1;
                    }
                    html += "</tr>";
                }
                html += "</table>";

                table.find("#" + id).html(html);
                id = null;
                document.getElementById('popupBox').style.display = "none";
            });

            $("#empty").click(function () {
                table.find("#" + id).html("&nbsp");
                document.getElementById('popupBox').style.display = "none";
            });
        }
    });

    //on cell click
    $("#close").click(function () {
        document.getElementById('popupBox').style.display = "none";
    });

    $("#giveUp").click(function () {
        for(var x = 0; x < 9; x++) {
            for(var y = 0; y < 9; y++) {
                table.find("#"+ x+"-"+ y).html(current_solution[x][y]);
            }
        }
    });
});
